def init_logging(islog=False,logpath='LOG'):
    text = """
    本目录是用来存放pytasknium的log,请勿随意删除!
    """
    try:
        if (logpath != 'LOG'):
            logpath = str(logpath).strip()
            logpath = logpath.rstrip("\\")

    except Exception as e:
        logpath='LOG'


    isExists = os.path.exists(logpath)
    if not isExists:
        os.makedirs(logpath)

    if(not os.path.exists(logpath+"\\readme.txt")):
        with open(logpath + "\\readme.txt", "w+") as f:
            f.write(text)
    formatter = '[%(asctime)s] [%(levelname)s]: %(message)s'
    if islog:
        logging.basicConfig(level=logging.INFO, format=formatter)
        filelog = logging.FileHandler(filename=logpath + "\\%s.log" % datetime.now().strftime("%H%M%S"),
                                      mode="a", encoding="utf-8")
        filelog.setLevel(logging.INFO)
        filelog.setFormatter(logging.Formatter(formatter, "%H:%M:%S"))
        logging.getLogger('').addHandler(filelog)
        hostlog(logging.getLogger('').handlers)
    else:
        logging.basicConfig(level=logging.INFO, format=formatter)

    return logpath+"\\"




