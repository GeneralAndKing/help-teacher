def log_time_delta(func):
    @wraps(func)
    def deco(*args,**kwargs):
        start = datetime.now()
        res = func(*args,**kwargs)
        end = datetime.now()
        delta = end - start
        print(func.__name__ +" time is ", delta)
        return res
    return deco



